function strhighlight($needle, $haystack){ 
    $ind = stripos($haystack, $needle); 
    $len = strlen($needle); 
    if($ind !== false){ 
        return substr($haystack, 0, $ind) . "<strong>" . substr($haystack, $ind, $len) . "</strong>" . 
            highlight($needle, substr($haystack, $ind + $len)); 
    } else return $haystack; 
}

